    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        try {
            Object objuser = cbSolicitante.getSelectedItem();
            Object objEqui = cbEquipamento.getSelectedItem();
            Usuario user = (Usuario) objuser;
            Equipamento equip = (Equipamento) objEqui;

            int solicitante = user.getId_usuario();//cbSolicitante.getSelectedItem().toString();
            int equipamento = equip.getId();//cbEquipamento.getSelectedItem().toString();
            String problema = txtProblema.getText();
            String diagnostico = txtDiagnostico.getText();
            String prioridade = cbPrioridade.getSelectedItem().toString();
            String data = dateChooserCombo1.getText();

            String status;

            if (checkbox1.getState()) {
                status = "ABERTO";
            } else {
                status = "FECHADO";
            }
//salvar(int idUsuario, int idEquipamento,String problemaRelatado, String diagnosticoTecnico, String prioridade,
            String msg = controller.salvar(solicitante, equipamento, problema, diagnostico, prioridade, status, data);

            JOptionPane.showMessageDialog(null, msg);

            listarTabela();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

