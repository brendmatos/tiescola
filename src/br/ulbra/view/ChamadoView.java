    public void listarTabela() {
        DefaultTableModel model = (DefaultTableModel) tblChamado.getModel();
        model.setRowCount(0);
        // Pegamos o usuário da sessão

        List<Chamado> chamados = controller.listar();

        for (Chamado c : chamados) {
            model.addRow(new Object[]{
                c.getId(),
                c.getIdUsuario(),
                c.getIdEquipamentoTag(),
                c.getProblemaRelatado(),
                c.getDiagnosticoTecnico(),
                c.getPrioridade(),
                c.getStatus(),
                c.getDataAbertura()
            });
        }
    }

